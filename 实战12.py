print('在古希腊神话中,玫瑰集爱情与美丽于一身,所以人们常用玫瑰来代表爱情但是不同的朵数的玫瑰花代表的含义是不同的')
number=int(input('请输入您想送几朵玫瑰花,我会告诉你玫瑰花的含义:'))
if number == 1:
    print('1朵:你是我的唯一')
elif number == 3:
    print('3朵:I love you')
elif number == 10:
    print('10朵:十全十美')
elif number == 99:
    print('99朵:天长地久')
elif number == 108:
    print('108朵:求婚')
elif number == 999:
    print('999朵:土豪')
else:
    print('我也不知道了,你可以考虑送1朵.3朵.10朵.99朵.108朵.999朵') 
